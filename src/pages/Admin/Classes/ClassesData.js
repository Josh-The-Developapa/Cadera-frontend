export function assignRandomAvatars(classesData) {
  const output = classesData.map((classObj) => {
    if (classObj.teachers && Array.isArray(classObj.teachers)) {
      classObj.teachers = classObj.teachers.map((teacher) => {
        const randomNum = Math.floor(Math.random() * 70) + 1;
        return {
          ...teacher,
          avatar: `https://i.pravatar.cc/150?img=${ randomNum }`,
        };
      });
    }

    return classObj;
  });
  return output;
}

